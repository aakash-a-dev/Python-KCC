# DataType Output: str
x = "Hello World"
print(x)

# DataType Output: int
x = 50
print(x)

# DataType Output: float
x = 60.5
print(x)

# DataType Output: complex
x = 3j
print(x)

# DataType Output: list
x = ["aakash", "amod", "rajput"]
print(x)

# DataType Output: tuple
x = ("aakash", "amod", "rajput")
print(x)

# DataType Output: range
x = range(10)
print(x)

# DataType Output: dict
x = {"name": "Aakash", "age": 21}
print(x)

# DataType Output: set
x = {"Aakash", "Amod", "Rajput"}
print(x)

# DataType Output: frozenset
x = frozenset({"Aakash", "Amod", "Rajput"})
print(x)

# DataType Output: bool
x = True
print(x)

# DataType Output: bytes
x = b"Aakash"
print(x)

# DataType Output: bytearray
x = bytearray(4)
print(x)

# DataType Output: memoryview
x = memoryview(bytes(6))
print(x)

# DataType Output: NoneType
x = None
print(x)
